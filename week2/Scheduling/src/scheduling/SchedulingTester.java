    /**
     * This method compares the different scheduling algorithms on the same test set (list of tasks and number of machines).
     *
     * @param tasks the list of tasks to sort
     * @param nbMachines the number of machines in the workshop
     * @param schedulingAlgorithms the scheduling algorithms to test
     * @param verbose true if you want to display the state of the workshop after
     * each of the scheduling algorithms, false if you want to display
     * only the workshop criteria (completion time and penalty cost)
     */
    public static void compareSchedulingAlgorithms(
            List<Task> tasks,
            int nbMachines,
            List<Scheduling> schedulingAlgorithms,
            boolean verbose) {
        Workshop w1 = schedulingAlgorithms.get(0).schedule(nbMachines, tasks);
        w1.display(verbose); 
    }

